import mongoose from "mongoose";

const LoginCredSchema = mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const LoginCredentials = mongoose.model("LoginCredentials", LoginCredSchema);
module.exports = LoginCredentials;
